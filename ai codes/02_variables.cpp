// 02_variables.cpp
// Amaç: Değişkenler, temel veri tipleri ve atama işlemleri

#include <iostream>

using std::cout;
using std::endl;

int main() {
    // Integer (tam sayı)
    int yas = 25;

    // Double (ondalıklı sayı)
    double pi = 3.14159;

    // Char (tek karakter)
    char harf = 'A';

    // Boolean (mantıksal)
    bool dogruMu = true;

    cout << "yas: " << yas << endl;
    cout << "pi: " << pi << endl;
    cout << "harf: " << harf << endl;
    cout << "dogruMu: " << dogruMu << endl; // true -> 1, false -> 0

    return 0;
}
